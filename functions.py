def square(side):
    area = side * side
    print(" The area of the square is" + str(area) + "square units")


def circle(radius):
    pi = 3.14
    area = pi * radius * radius
    print(
        "The area of the circle is " + str(area) + "square units"
    )  # What does the str


square(8)
circle(10)
